#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*s;

	if (size + 1 < size)
		handle_error(ERR_MALLOC_SIZE);
	s = (char *)ft_memalloc(sizeof(char) * (size + 1));
	return (s);
}
